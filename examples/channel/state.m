format compact

gamma=1.4
H = 1.0 % height of channel
L = 5   % length of channel

R = 1;
mach=0.1;
%-------------------------------------------------------------------------------
% couette flow
%-------------------------------------------------------------------------------
u_inf=1
Re=100
rho_inf=1;

c_inf = u_inf / mach;
T_inf = c_inf^2 / (gamma * R)
p_inf = R * rho_inf * T_inf
mu_inf = rho_inf * u_inf * H / Re

fprintf(1,'------------------------------------------------\n')
%-------------------------------------------------------------------------------
% pouiseille flow
%-------------------------------------------------------------------------------
p_out = p_inf - u_inf * 8 * mu_inf * L
